<h2> Meeting minutes</h2>
                                 <p> <div class = "ms-3"> <small> of the technical commission of sporting on 18.03.2022 </small> </div> </p>


                                 <p> 1) It is decided to shoot singles and doubles together during the 2022 season to reduce downtime during the competition.

                                 <p> 2) The general ranking in % will be calculated only for those shooters who participated in at least 5 out of 8 contests

                                 <p> 3) In the national final, only the athlete who participated in at least 4 stages before the final enters the calculation on categories A, B, C

                                 <p> 4) Enrollment in competitions must be made no later than the Thursday before the start of that competition at 24.00
     <ul>
         <li> 350 RON is the registration fee </li>
         <li> 50 RON is the penalty charged to those who register after this deadline (last 24 hours before the start of the competition) </li>
         <li> Cancellations can be made free of charge but at least 24 hours in advance. Those who cancel in the last 24 hours or do not announce at all that they will no longer participate will have to pay a fee of 100 RON </li>
     </ul>
 
     <p> 5) The draw will be made on the Friday of the competition either by a committee of 5 shooters or automatically by the application from www.fitascsporting.ro

     6) On the Friday before the start of the competition, a three-person disciplinary committee will be set up.
        <ul>
            <li> Organizer's representative plus two experienced shooters </li>
            <li> The fee for filing an appeal is 200 RON </li>
        </ul>